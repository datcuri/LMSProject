    private void cbDanhmucItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbDanhmucItemStateChanged
        // TODO add your handling code here:
        if (checkLoading) {
            DefaultTableModel Model = (DefaultTableModel) TableSach.getModel();
            String danhmuc = cbDanhmuc.getSelectedItem().toString();
            String theloai = cbtheloai.getSelectedItem().toString();

            if (danhmuc.equalsIgnoreCase("Danh mục") == false
                && theloai.equalsIgnoreCase("Thể loại") == false) {

                Model.setRowCount(0);
                for (Sach sach : CtrSach.CtrgetSachbyBoth(danhmuc, theloai)) {
                    Model.addRow(new Object[]{sach.getMaSach(), sach.getTenSach(), sach.getTacgia(), sach.getNhaXuatBan()});
                }

            } else {
                if (danhmuc.equalsIgnoreCase("Danh mục") == false
                    && theloai.equalsIgnoreCase("Thể loại") == true) {

                    Model.setRowCount(0);
                    for (Sach sach : CtrSach.CtrgetSachbySort(danhmuc, "danhmuc")) {
                        Model.addRow(new Object[]{sach.getMaSach(), sach.getTenSach(), sach.getTacgia(), sach.getNhaXuatBan()});
                    }

                } else {
                    if (danhmuc.equalsIgnoreCase("Danh mục") == true
                        && theloai.equalsIgnoreCase("Thể loại") == false) {

                        Model.setRowCount(0);
                        for (Sach sach : CtrSach.CtrgetSachbySort(theloai, "theloai")) {
                            Model.addRow(new Object[]{sach.getMaSach(), sach.getTenSach(), sach.getTacgia(), sach.getNhaXuatBan()});
                        }

                    } else {
                        filldataTableSach();
                    }
                }
            }
        }
    }//GEN-LAST:event_cbDanhmucItemStateChanged

