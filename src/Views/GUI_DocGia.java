    public boolean KiemtraSLmuon(String masach) {
        DefaultTableModel Model = (DefaultTableModel) TableDSmuon.getModel();
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(Model);
        sorter.setRowFilter(RowFilter.regexFilter(masach));
        return sorter.getViewRowCount() != 0;
    }

    public void xacnhanmuonsach(Sach sach) {
        DefaultTableModel Model = (DefaultTableModel) TableDSmuon.getModel();
        if (KiemtraSLmuon(sach.getMaSach())) {
            JOptionPane.showMessageDialog(this, "Bạn đã có sách này trong danh mục mượn", "", JOptionPane.PLAIN_MESSAGE);
        } else {
            Model.addRow(new Object[]{sach.getMaSach(), sach.getTenSach(), sach.getTacgia(), sach.getNhaXuatBan()});
            setSoluongmuon();
        }
    }

    public void loadmaTheLoai() {
        cbtheloai.removeAllItems();
        cbtheloai.addItem("Thể loại");
        String sql = "SELECT tentheloai FROM Theloai";
        try {
            Statement statement = conn.createStatement();
            ResultSet rs = statement.executeQuery(sql);
            while (rs.next()) {
                this.cbtheloai.addItem(rs.getString(1));
            }
        } catch (SQLException e) {

        }
    }

