    private void btnguiphieumuonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguiphieumuonActionPerformed
        // TODO add your handling code here:
        if (dangmuon == 0) {
            JOptionPane.showMessageDialog(GUI_DocGia.this, "Danh sách mượn trống!", "Thất bại", JOptionPane.ERROR_MESSAGE);
        } else {
            if (dangmuon > max) {
                if (max == 0) {
                    JOptionPane.showMessageDialog(this, "Bạn đã hết lượt mượn,"
                        + " trả sách đã mượn để được mượn tiếp!", "Thất bại", JOptionPane.ERROR_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Vượt quá số lượng sách được phép mượn! "
                        + "Hãy xóa bớt danh sách mượn", "Thất bại", JOptionPane.ERROR_MESSAGE);
                }
            } else {

                String maPM = "PM" + String.valueOf(CtrPM.CtrDemSoPM());

                Phieumuon phieuMuon = new Phieumuon();
                phieuMuon.setMaPhieumuon(maPM);
                phieuMuon.setMaDocgia(maDocgia);
                phieuMuon.setMaThuthu(null);
                phieuMuon.setSongaymuon(Integer.parseInt(String.valueOf(cbThoigianmuon.getSelectedItem())));
                phieuMuon.setGhichu(null);
                phieuMuon.setTrangthai("Chưa duyệt");

                CtrPM.CtrThemPhieumuon(phieuMuon);

                int rowCount = TableDSmuon.getRowCount();

                for (int i = 0; i < rowCount; i++) {
                    ChitietPhieuMuon ctpm = new ChitietPhieuMuon(maPM, String.valueOf(TableDSmuon.getValueAt(i, 0)),"Chờ duyệt");
                    CtrCTPM.CtrThemCTPhieumuon(ctpm);
                }

                max = max - rowCount;
                //              setGioiHanMuon(gioiHan);
                lbluotmuon1.setText(String.valueOf(max));
                CtrCTPM.CtrSuasoluongmuon(MaTaikhoan, 3 - max);

                JOptionPane.showMessageDialog(this, "Xác nhận thành công!"
                    + "\nMời bạn đến thư viện để nhận sách trong thời gian sớm nhất.");
                DefaultTableModel Model = (DefaultTableModel) TableDSmuon.getModel();
                Model.setRowCount(0);
                setSoluongmuon();
                //              setGioiHanMuon();

                filldatatableDSmuon(maDocgia);

            }
        }
    }//GEN-LAST:event_btnguiphieumuonActionPerformed

